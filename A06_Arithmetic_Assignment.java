



public class A06_Arithmetic_Assignment {
	public static void main(String[] args) {
		int a=123;
		System.out.println(a);
		a+=10;
		System.out.println(a);
		a-=10;
		System.out.println(a);
		a*=10;
		System.out.println(a);
		a/=10;
		System.out.println(a);
		a%=10;
		System.out.println(a);
	}

}
